package Test;

public class PrintStatement {

	public static void main(String[] args) {
		
		//Print - Pointer will be set at same line.
		//Println - Pointer will be set at next line.
		
		System.out.print("Hello"); 
		System.out.print("This"); 
		System.out.print("is");
		System.out.print("Java");
		
		System.out.println("Extra Line"); //will be print on same line, Println - pointer will be at next line
		System.out.print("New Line");
		System.out.println("Same Line");
		
		System.out.println("Selenium");
		System.out.print("Jira");
		
		System.out.println(); //When you want print next line as New line use empty syso.
	
		System.out.print("QTP");
		

	}

}
